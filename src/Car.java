public class Car {
    public String id;
    public String brand;
    public String model;
    public int releaseYear;
    public String color;
    public int price;
    public String registrationNum;

    Car(String id, String brand, String model, int releaseYear, String color, int price, String registrationNum) {
        this.id = id;
        this.brand = brand;
        this.model = model;
        this.releaseYear = releaseYear;
        this.color = color;
        this.price = price;
        this.registrationNum = registrationNum;
    }
}
